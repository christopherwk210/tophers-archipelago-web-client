import fs from 'node:fs/promises';
import path from 'node:path';
import { Readable } from 'node:stream';
import csv from 'csv-parser'

const sheetID = '1lFzteyQHUxV8EbwPVv8x5LmmEkWN4SDU77boTkO9I-c';
const url = `https://docs.google.com/spreadsheets/d/${sheetID}/gviz/tq?tqx=out:csv&sheet=Localization&range=A3:Z1000`;

const __dirname = import.meta.dirname;
const dataFilePath = path.join(__dirname, '../src/localization.ts');

async function main() {
  const response = await fetch(url).catch(e => console.log(e));
  if (!response) {
    console.error('Failed to fetch localization data.');
    return;
  }

  const csvData = await response.text().catch(e => console.log(e));
  if (!csvData) {
    console.error('Failed to parse localization data.');
    return;
  }

  // { Notes: string; Category: string; ID: string; [language: string]: string; }[]
  const data = await new Promise(resolve => {
    const results = [];
    Readable.from([csvData])
      .pipe(csv())
      .on('data', row => results.push(row))
      .on('end', () => resolve(results));
  });

  const messages = {};
  for (const row of data) {
    const { Notes, Category, ID, ...translations } = row;

    for (const [language, translation] of Object.entries(translations)) {
      const lang = language.toLowerCase();
      if (!messages[lang]) messages[lang] = {};
      if (!messages[lang][Category]) messages[lang][Category] = {};
      messages[lang][Category][ID] = translation;
    }
  }

  const cleanedJSON = JSON.stringify(messages, null, 2);
  const now = new Date();
  const output =
    '// Do not edit manually - This file is autogenerated by ./tools/parse-localization.js\n' +
    '// Autogenerated on ' + now.toLocaleString() + '\n\n' +
    'export const i18n_messages = ' + cleanedJSON + ' as const;\n\n' +
    'type Base = typeof i18n_messages[\'en\'];\n' +
    'export type LocaleKey = { [C in keyof Base]: `${C}.${keyof Base[C] & string}` }[keyof Base];\n'

  const success = await fs.writeFile(dataFilePath, output).catch(e => { console.log(e); return false; });
  if (success === false) {
    console.error('Failed to write localization data.');
  } else {
    console.log(`Successfully wrote localization data to ${dataFilePath}`);
  }
}

main();